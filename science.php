            <h3>Why AQA for GCSE Combined Science: Trilogy?</h3>
            <p>We believe that science has something to offer every student and AQA provides the best specification and support resources to suit students of all abilities and all aspirations.</p>
            <p>The course is made up of 6 exams; 2 Biology, 2 Chemistry and 2 Physics. Questions are based on content and required practicals the students are expected to have covered.</p>

            <h4>AQA ensure that:</h4>
            <ul class="circle">
                <li>The Biology, Chemistry and Physics content is presented clearly, in a logical teaching order.</li>
                <li>The subject content and required practicals are also in our Biology, Chemistry and Physics GCSEs so you have the flexibility to co-teach or to move your students between courses.</li>
                <li>All science qualifications provide opportunities for progression.</li>
                <li>Combined Science: Trilogy gives students the option to progress to A-levels in science or other subjects.</li>
            </ul>

            <p>Required practicals have been trialled by teachers previously and there’s no better way to learn about science than through purposeful practical activities as part of day to day teaching and learning.</p>

            <h4>The 21 required practicals:</h4>
            <ul class="circle">
                <li>are clearly laid out in the specification, so you know exactly what’s required.</li>
                <li>are deliberately open, so they can be taught in the way that suits the students.</li>
            </ul>

            <p>Students are also issued with required practical booklets for each biology, chemistry and physics. These include the practical method, results and application questions.</p>

            <h4>AQA have ensured their exams:</h4>
            <ul class="circle">
                <li>use more straightforward language and fewer words so they’re easier to understand.</li>
                <li>have fewer contexts so students don’t get confused.</li>
                <li>have questions that increase in difficulty, so students feel confident.</li>
                <li>have been written with our GCSE Mathematics and A-level science teams, so students have consistency between content and questions.</li>
            </ul>


            <h3>Purpose</h3>
            <p>A high-quality science education provides the foundations for understanding the world through the specific disciplines of biology, chemistry and physics. Science has changed our lives and is vital to the world’s future wealth, and all pupils should be taught essential aspects of the knowledge, methods, processes and uses of science.</p>
            <p>Pupils will build up a body of foundational knowledge and concepts, they will be encouraged to use rational explanation and develop a sense of excitement and curiosity about natural phenomena. They should be encouraged to understand how science can be used to explain what is occurring, predict how things will behave, and analyse causes.</p>

            <h3>Aims</h3>
            <p>The science curriculum aims to ensure that all pupils:</p>
            <ul class="circle">
                <li>Develop scientific knowledge and conceptual understanding through the specific disciplines of biology, chemistry and physics.</li>
                <li>Develop understanding of the nature, processes and methods of science through different types of science enquiries that help them to answer scientific questions about the world around them.</li>
                <li>Are equipped with the scientific knowledge required to understand the uses and implications of science, today and for the future.</li>
            </ul>


            <h3>Purpose</h3>
            <p>The purpose of religious education (R.E) in Hampshire, Portsmouth and Southampton is to support students in developing their own coherent patterns of values and principles, and to support their spiritual, moral, social, and cultural development.  In Park Community School the terms ‘Religion and Ethics’ at KS3 and ‘Ethics and Philosophy’ at KS4 are used to signify our focus on the development of personal patterns of values in the light of contemporary ethical and social issues facing society.</p>
            <p>The agreed syllabus which provides the legal framework for R.E states that “we have to understand how and why people live differently from each other and respect their right to do so. Living Difference identifies that respecting difference requires us to engage with difference confidently. We need to identify how and why we have different beliefs, attitudes and practices from other people. By doing this, we can gain respect from others. Accepting difference does not imply agreement; nor does it imply that all difference is acceptable. It is important that students make reasoned and informed judgements about difference and that these are based on beliefs and values that they sincerely hold, and that they can express and defend in reasoned debate. Teachers have a responsibility for helping students to develop their beliefs and values, in relation to those values that society prizes. As a result, the agreed syllabus places particular importance upon the spiritual, moral, social and cultural development of students."</p>
            <cite>Agreed Syllabus, ‘Living Difference’, for Hampshire, Portsmouth and Southampton</cite>
            <p>The purpose of R.E, Ethics and Philosophy at Park is therefore to promote pupils spiritual, moral, social and cultural development through an awareness and appreciation of diversity and difference of beliefs and values, cultures and societies within the UK and the wider world.  As part of this pupils are offered the chance to respond and in so doing develop their own sense of self and place in the local, national and global community.</p>

            <h3>Aims</h3>
            <ul class="circle">
                <li>Encourage each student to interpret and respond to a variety of concepts, beliefs and practices within religions and to their own and others’ cultural and life experiences.</li>
                <li>Students will develop the capacities to interpret, evaluate and respond to differing values and beliefs.</li>
                <li>This can be achieved through extending their thinking and analytical skills and their creative, imaginative and emotional development.</li>
                <li>R.E aims to foster mutual understanding between students of differing religious and cultural backgrounds.</li>
                <li>R.E aims to promote an awareness of differing understandings of human experience and ways in which religions view the purpose of life.</li>
                <li>Pupils and students are encouraged to develop their own concepts to interpret their own experiences, and explain the experiences of others in the wider world.</li>
            </ul>

            <h3>Areas of Study</h3>
            <h5>Year 7</h5>
            <ul class="circle">
                <li>The Island - Pupils explore concepts such as community and tradition common to many religious traditions and human experiences.  They are offered the opportunity to explore what these concepts mean to them as individuals and the society in which they live.</li>
                <li>Equality - Pupils will explore how women’s rights have varied across time and countries.</li>
                <li>The Media - Pupils will examine the impact, role and use of the media in relation to religion and associated issues.</li>
                <li>Standing up for your beliefs - Pupils will explore how and why people have stood up for what they believe in.  The focus is on religion and the environment with a case study looking at Chico Mendez.</li>
            </ul>

            <h5>Year 8</h5>
            <ul class="circle">
                <li>Being Human - Pupils will explore some of the big questions facing human beings such as, why are we here?  Is there a purpose to existence?  They will begin to examine arguments for and against the existence of God.  Pupils will be encouraged to explore and form their own responses to these questions.</li>
                <li>Human Footprint - Pupils will explore the impact of war on people’s lives thinking about the concept of ‘sacrifice’ within Christianity.  In particular they will investigate the lives of Christian prisoners of war and those who gave their life for their beliefs in this period.  Pupils will have the space to consider their own most deeply held beliefs and what they are prepared to stand up for.</li>
                <li>Extreme World - Pupils will take an in-depth look at Islam followed by an investigation into the rise of radical groups within Islam and Christianity and how religious believers are fighting back against such interpretations of their faith.  Pupils are asked to consider what role the media has in portraying religious traditions and to what extent this portrayal and more extreme groups in general are an accurate reflection of the religious tradition.</li>
            </ul>

            <h5>Year 9</h5>
            <p><em>In years 9-11 students have one lesson a fortnight of core Ethics and Philosophy</em></p>
            <ul class="circle">
                <li>Buddhism A Journey to Happiness - Students explore the basic beliefs of Buddhism and to what extent these might hold the key to living a good life.  They will be given the opportunity to respond to the ideas of this religion and explore whether these can be applied to non-religious believers.</li>
                <li>Islamaphobia - Students will explore attitudes towards Islam and Muslims in the UK and the role of the media in this.</li>
                <li>The Kite Runner - Students will explore the links between the UK and Afghanistan through the film the ‘Kite Runner’ investigating a range of issues including refugees, ethnicity, drugs and conflict.</li>
            </ul>

            <h5>Year 10</h5>
            <ul class="circle">
                <li>Marriage and Community - Students will explore issues of relationships and family with a particular focus on Christian and Muslim attitudes.  They will investigate the changing nature of the family in the UK today.</li>
                <li>Tolerance, politics and community - This unit looks at the place and role of religion in British society today.  They will explore different political systems both religious and secular and the types of communities that these systems create.</li>
                <li>Money matters - This unit explores the role of money and wealth in society, how different religious traditions view this and how they put these beliefs into practice.</li>
            </ul>


            <h5>Year 11</h5>
            <ul class="circle">
                <li>Sanctity of Life - Students begin this unit by investigating attitudes towards abortion and euthanasia.  They are then able to choose which issue they would like to focus on for independent research with a focus on whether the current legal guidelines in the UK should remain or change.</li>
                <li>Capital Punishment - This unit focuses on the debate as to whether the death penalty should be brought back to the UK.</li>
                <li>Animal Testing - Animal testing is widely used in the UK for both medical and cosmetic purposes.  Students will consider different religious responses to this question before having the opportunity to develop their own personal response.</li>
            </ul>

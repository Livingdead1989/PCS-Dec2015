            <h4>Pupil Premium impact 2013-2014</h4>
            <p><strong>Achievement</strong> – In August 2014 provisional exam data indicated a gap between PP and non-PP attainment of 9% for 5AC EM.  PP attainment increased by 5% in 2014 from 2013.</p>
            <p>The Pupil Survey 2013-2014 for Year 11 reveals 96% of students believed they had been taught well during their time at Park Community School, with 81% finding study clubs helpful.</p>
            <p>Of the students offered additional English/Maths tuition, 92% found it to be helpful.</p>

            <p><strong>Attendance</strong> – students being able to access appropriate programmes and receive relevant support has meant that there were only 5 persistent absentees in Year 11 students on Free School Meals.</p>

            <p><strong>Residential, Social and Cultural visits</strong> – the school has continued to support funding for a number of international, residential and cultural visits for identified students, in addition to school subsidy and the governors’ subsidy on many more local school trips.</p>
            <p>As a result of the English residential in March 2014, 81% of Pupil Premium students stated they felt more confident about their writing exam.  In addition, 100% of Pupil Premium students believed they had learnt one new strategy they could apply to their writing when they returned to school.</p>

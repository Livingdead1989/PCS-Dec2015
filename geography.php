            <h3>Purpose</h3>
            
            <p>Geography is primarily concerned with promoting an understanding of the nature of the Earth’s surface and more particularly the character of places, the complex nature of people’s relationships and interactions with their environment, and the importance in human affairs of the location and spatial organisation of human activities. This understanding includes the development of critical awareness of problems and conflicts in the geographical environment which relates to differing attitudes and situations. In this social context geography can also be a valuable medium for developing an understanding and sensitivity to multicultural communities and societies.</p>

            <h3>Aims</h3>
            <p>Through the variety of teaching and learning strategies involved in Geography, pupils will form an understanding of the different physical, human and environmental processes that are an integral part of our world.</p>

            <ul class="circle">
                <li>Geography seeks to explore the relationship between the Earth and its people through the study of 'place', 'space' and 'environment'. Students will ask the questions 'where', 'what', 'who', 'how' and 'why'.</li>
                <li>The study of 'place' is aimed to allow students to describe and understand not only the location of physical and human features of the Earth, but also the processes, systems and interrelationships that create or influence those features.</li>
                <li>The study of 'space' teaches students to explore the relationships between places and patterns of activity arising from the use people make of the physical setting where they live and work.</li>
                <li>The study of the 'environment' embraces both its physical and human dimensions. Thus it addresses the resources, sometimes scarce and fragile that the Earth provides and on which all human life depends; the impact on those resources of human activities; and the wider social, economic, political and cultural consequences of the interrelationship between the two.</li>
            </ul>

            <p>The overall implications of sustainability both of land, resources, and human actions will promote the development of knowledge, skills, understanding and values in pupils so they make responsible decisions, both individually and collectively, at both local and global scales, about the environment.</p>
            
            <h3>Areas of Study</h3>
            <p>Geography runs in themes across different year groups, tying in with the STAR curriculum in Year 7, Humanities in Year 8 and the overarching GCSE themes from Year 9 to 11.</p>

            <p>In Year 7 students concentrate on the key skills required for successful study of Geography throughout their school career, starting with map skills. In our ever-changing world the alarming inequalities, particularly with women’s rights.  Pupils enquire after what countries that don’t meet the Millennium Development Goals and make comparisons between More and Less Economically Developed Countries. Understanding the pressure on global resources is paramount therefore students study the Tropical Rainforest and begin looking at the concept of sustainability.</p>

            <p>In Year 8 students start looking at more in-depth topics to examine the human and physical world.</p>

            <p>The Earth’s population will be 9 billion by 2050. As the stress of 7 billion people takes its toll on the Earth’s resources, we study how different people are affected by changes in population and the power of the earth itself. How many more people can the Earth take? In the spring term students study the atmosphere and tectonics. The outcome of this unit is to be able to produce and deliver a detailed weather forecast from synoptic charts and data.</p>

            <p>Extreme weather is an increasing problem in the 21st century and therefore crucial to understand. Tectonics looks at the natural hazards earthquakes, volcanoes, and tsunamis. The focus is on the impacts of these events with close links to Science. Media plays an important role in this unit which builds in ‘Geography in the News’.  The summer term looks at Wild Water and Extreme environments. Wild Water focuses on the idea of climate change and our findings about global population, making detailed links between units from both Year 7 and 8. How does climate affect the lives of people around us? Are some people in the world affected more than others? How can we protect our futures in a sustainable way? Extreme World examines the coldest and driest environments and how we adapt.  How did these magnificent landscapes come to exist?</p>

            <p>GCSE Geography teaching starts in Year 9 following main themes: People and Planet (population. Resources, globalisation, development, settlements in the UK, and challenges in urban areas). Dynamic Planet (Battle for biosphere, climate and change, coasts, extreme climates, restless Earth and water world.  These topics are all in-depth studies to broaden the geographical understanding of all pupils and prepare them for the world of work. All pupils will have the opportunity to complete field work to put theory into practice.</p>
            

            

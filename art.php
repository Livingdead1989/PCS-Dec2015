            <h3>Purpose</h3>
            <p>Art, craft and design embody some of the highest forms of human creativity. At Park we believe that high quality art and design should engage, inspire and challenge pupils; equipping them with the knowledge and skills to experiment, invent and create their own works of art, craft and design. As pupils progress, they will be able to think critically and develop an understanding of art and design. Pupils will learn how art and design both reflect and shape our history, and contribute to the culture, creativity and wealth of our nation and beyond.</p>

            <h3>Aims</h3>
            <p>All pupils will have the opportunity to produce creative work by exploring their ideas and recording their experiences in a variety of ways using a range of materials, processes and techniques. Pupils will become proficient in drawing, painting, sculpture and other art, craft and design techniques whilst also developing their use of art vocabulary when evaluating and analysing creative works. Pupils will also develop their subject knowledge and understanding through the identification of key artists, craft makers and designers that have been key in the historical and cultural development of the subject.</p>
            
            <h3>Areas of Study</h3>
            <p>Pupils are taught to develop their creativity and ideas through a critical understanding of artists, architects and designers, which combined with mastery of key technical skills can then be used to inform their own work. Pupil projects are designed to ensure that all pupils know about the history of art, craft, design and architecture, including periods, styles and major movements and direct links are made throughout the duration of the project work.</p>
            <p>Throughout all key stages pupils are given the opportunity to use a range of techniques to record their observations in sketchbooks as a basis for exploring their ideas. Pupils are expected to experiment with a varied range of media, including painting, drawing, mixed media techniques, collage, clay, photography, digital manipulation packages, textiles and oil pastels. Pupils will revisit media techniques and processes many times to allow for mastery of the skill.</p>
            <p>Pupils will continually analyse and evaluate their own work and the work of others in order to understand how to adapt and refine their own work to ensure the most competent visual image is presented, or the best application for their work has been selected.</p>

            

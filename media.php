            <h3>Intent</h3>
            <p>In Media we equip pupils to understand existing media texts, carefully plan their own media texts, create media texts in a professional manner, then evaluate them effectively. Media is all about working in a professional way, using technical terminology, skills and equipment in order to create fully realised products for specific audiences. A further fundamental in media is cultural capital. We live in a media led society and this course encourages pupils to understand how media works, how it impacts them and how it is constantly evolving. They will consume a wide range of media texts designed for a variety of audiences, opening their eyes to  the media world outside their immediate sphere of knowledge.
            </p>

            <h3>Implementation</h3>
            <p>Our curriculum begins in year 9 and aims to give pupils an introduction to the world of pre-production skills before they begin working on coursework units. Lessons encourage an ethos of independence and provide pupils with opportunities to work both individually and within groups. Further opportunities to develop media skills are built in to ensure they can make informed choices about which path they take.  Coursework teaching is structured to cover the 4 assessment areas required:
</p>
            <ul class="circle">
                <li>LO1 - Pupils will learn how to understand the purpose and content/uses/properties of existing media texts. They will research existing examples and deconstruct to ensure they show clear understanding of how, why and for whom they have been made.
                </li>
                <li>LO2 - Pupils will learn how to plan their own media texts. They will begin by deconstructing and understanding a product brief. They will then learn about a wide range of potential planning documents, before showing their understanding by choosing and using the correct documents for the type of text they are going to create.
                </li>
                <li>LO3 - Pupils will learn how to create a media text. They will adopt professional working practices where possible and use technical equipment and software to create a product for a specific audience which responds clearly to a brief.
                </li>
                <li>LO4 - Pupils will learn how to review their own and other pupilâ€™s work. They will be able to explain how well they have met the product brief, how successful their text is and how they would adapt their work in future if they were to do something similar again.
                </li>
            </ul>
            <p>Due to the nature of the course we are able to give pupils a degree of ownership over how the course is organised for them. While the first 2 units are prescribed by the exam board, subsequent units are chosen by classes after the list of unit choices is presented to them. There is potential for multiple units to be taking place within one class depending on the level of independence and capability of the pupils within the group.
            </p>

             
            <h3>Impact</h3>
            
            <ul class="circle">
                <li>Pupils have a appreciation of media and how powerful it is in shaping modern society.</li>
                <li>Pupils will have a critical appreciation both of the work of others and the work they are able to create themselves.</li>
                <li>Pupils leave us with a broader cultural capital, a deeper understanding and appreciation of the world around them and the ability to see the creative potential of a range of situations.</li>
                <li>Pupils are able to confidently use subject specific vocabulary when describing, analysing and evaluating the media texts of others and themselves.  </li>
                <li>Pupils are able to work independently, organise themselves, use equipment confidently and problem solve how to get the results they want to achieve.</li>
            </ul>


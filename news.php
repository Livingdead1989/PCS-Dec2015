            <h4>LACA Award</h4>
            <p><strong>Park Community School named as LACA Secondary School Catering Team of the Year.</strong></p>
            <p>Thursday 12th July 2018 Park Community School was named Secondary School Catering Team of the Year at the Lead Association for Catering in Education Awards (LACA) held at the Hilton Birmingham Metropole.</p>
            <p>Every year the LACA Awards for Excellence acknowledge individuals and teams of people who continue to make a real difference in education catering. Good management, strong performance and high achievement is recognised.</p>
            <p>Park Community School are proud to be ‘Much More Than Just a School’ and their catering team encompass this through providing affordable, quality food throughout the year including Christmas Day. Catering from nursery through to retirement, their passion shines through in providing free meals for their community through the MUNCH programme in the school holidays.</p>
        </div>

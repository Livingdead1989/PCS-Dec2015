                <li>Eat Out, Eat Well – <strong>Gold Award</strong> <em>(Hampshire County Council)</em>.</li>
                <li>Fair Trade Awards – Accredited Active and Aware school</li>
                <li>Reached the finals of Essential Cuisines - <strong>Second in Britain’s Best Brigade 2017</strong>.</li>
                <li>Ecucatering Excellence Awards 2016 - <strong>Winner Newcomer of the Year Award Steven Cross</strong>.</li>
            </ul>
            

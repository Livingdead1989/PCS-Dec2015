                        <p>Computer systems and programming</p>
                        <p>Candidates should develop a mental model of a computer system which comprises hardware and software and in which:</p>
                        <ul class="circle">
                            <li>Data is input and converted into the computer’s internal representation by input devices</li>
                            <li>The data is processed</li>
                            <li>The results of the processing are converted from the computer’s internal representation and output by an output device</li>
                            <li>The data may be stored for later use or transmitted to another computer system while it is still in the computer’s internal representation.</li>
                        </ul>
                        <br>
                        <p>Practical Investigation</p>
                        <p>Candidates will be expected to produce a report which will then be assessed under the four headings:</p>
                        <ul class="circle">

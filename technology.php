			<h3>Year 7 Design and Technology transition curriculum </h3>

			<p>Year 7 Design and Technology at Park is designed to enable our students to make a successful transition from Key Stage 2. Students will study three main areas. Our Core curriculum where students are learning the foundations of the subject. Design and Technology where students are learning the design process and health and safety legislation whilst using new equipment. They will also study Food and Catering including health and hygiene basics and cooking skills.</p> 

			<h3>Year 8 Design and Technology curriculum</h3>

			<p>Year 8 Design and Technology students’ study both DT and Hospitality and Catering. This is a skills-based year where students will design ad create a few products as well as learn new cooking skills. The aim of this year is to allow students to practise skills whilst trying to refine their work to improve its quality. There is a larger emphasis on the theoretical work to ensure that students are fully prepared for GCSE but we are fully supportive of teaching practical skills for life.</p>

			<h3>Year 9 Design and Technology curriculum</h3>

			<p>Year 9 Design and Technology students’ study Design and Technology, Hospitality and Catering as well as APEX construction skills. This is another skills-based year but where students try to master the skills learnt in year 7 and 8. This will also be an opportunity to learn new higher-level skills to prepare them for their GCSE years. The main aim of this year is to allow students to have time to practise and really refine their skills to develop their final outcomes and appreciate the need for a quality product. There is a larger emphasis on three areas for DT. They are Research – Analyse – Respond. This will support their practises in GCSE Art and Design. Students learning construction will learn skills for life as well as preparing them for Level 2 Construction in Multi-trades. Dishes cooked in Catering will be presented to a higher standard to ensure that all health and hygiene rules apply in more complex dishes. This preparation will allow students to succeed in their vocational qualification in Hospitality and Catering.</p> 


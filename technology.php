            <h3>What we stand for</h3>
            <p>We believe that Design, Technology, Food, Catering and Horticulture provide problem solvers of the future. A Park Design and Technologist innovates solutions through theoretical and practical application to develop creatively products from conception to conclusion. A learner’s problem solving ability is vital in employment and further education as well as working collaboratively in a team. A successful Design and Technologist can adapt their practise to work more efficiently, be decisive in their approach to learning new skills whilst applying them in education and in ‘real life’ situations. We believe that these skills will enhance our ambition for our learners to strive for excellence.</p> 


            <h3>The Purpose of Food and Design Technology</h3>
            <p>Design and technology is an inspiring, rigorous and practical subject. Students studying it will use their creativity and imagination to design and make a range of products that solve a variety of issues. Whilst designing, students will take into account other individuals needs as well as their own to produce the most effective outcome. Design and Technology requires a broad subject knowledge that draws upon other areas of the curriculum, for example, science engineering and maths. Pupils learn how to take risks and are required to design innovatively to produce new and interesting products and concepts.</p>

            <h3>Aims</h3>
            <p>All pupils will develop their creative, technical and practical skills to be able to partake in using new technologies successfully. Students will need to develop their knowledge and understanding of the subject so that they are able to design high-quality products and prototypes for a variety of end users. Most importantly students will learn to have a critical eye when looking at a product to analyse their strengths, weaknesses and suggest possible improvements, as well as evaluate the success of their work.</p>

            <h3>Areas of Study</h3>
            <p>Students will learn why conducting extensive research from a range of cultures and being able to understand a variety of needs is important when designing.</p>
            <p>Being able to produce a design brief and understanding how this is used in industry will enable students to keep their design focused and relevant.</p>
            <p>Effective problem-solving skills are vital for the students to critique their work and give careful thought about how this item could be developed or further changed and im-proved.</p>
            <p>Students will learn a variety of hand skills with tools and also machine processes. This will enable them to create an item, prototype, or product that will fit within their speci-fication parameters, and show their skill.</p>
            <p>A range of materials will be considered and will enable students to be critical but selective with their decision-making process to create a final outcome.</p>
            <p>Analysis of past and present designs will assist the students in understanding the limitations and possibilities that designing and creating can present.</p>
            <p>Testing and learning about new technologies are important parts of learning as this will enable students to show that they are becoming thoughtful designers. They will use their knowledge and responsibility as an environmentally considerate designer to create a sustainable product.</p>
            <p>Whilst developing their skills as designers students will need to be able to describe why a good technical understanding will help them create a more effective outcome. Individuals will be responsible for learning about the changes in circuitry and how programmable components can be tailored to suit different individual needs.</p>


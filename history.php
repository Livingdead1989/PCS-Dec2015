            <h3>Purpose</h3>
            <p>Understanding the world around us, where we have come from and how we got here is a journey of fascinating discoveries which allows students to understand the complexities of human beings and their development throughout history. Having a sense of the foundations of British history, and how the wider world has progressed, is key to learning the achievements and mistakes of the past, whilst deciding what is wanted for the future. As members of the next generation of voters and change makers in our political system, encouraging pupils to care about the wider communities of nations and human beings is a vital part of their education in History. We want our young people to have a coherent knowledge and understanding of the most significant events and to question all perspectives, interpretations and details. Weighing evidence and producing thoughtful arguments that show development of their own judgement are the transferrable skills that will ultimately make them successful learners and thoughtful human beings.</p>

            <h3>Aims</h3>
            <p>Developing enquiring and critical minds that have a genuine thirst for knowledge in this challenging subject is at the heart of the curriculum. Covering a wide span of time periods and places allows pupils to gain a coherent understanding of how Britain has been shaped into what we see and know today, along with the wider world. From ancient civilisations to the twentieth century, studying local, regional, national and international affairs will allow students to make connections, spot trends and identify contrasts between communities and approaches. Economic, cultural, military, political, and religious and social studies frames the study of different civilisations and allows pupils to examine historical concepts such as significance, causation , interpretation, change and continuity, consequence, diversity and enquiry.</p>
            
            <h3>Areas of Study</h3>
            <p>Timeless themes that run through the course of history will be our chosen approach to the structure of our Key Stage 3 curriculum; within this, there is a clear chronology that allows pupils to develop their sense of time. In Year 7, the first theme studied by pupils is ‘ordinary lives’. This unit sees the study of:  Ancient Rome, questioning how ‘civilised’ the Romans were; moving on to life as a Medieval peasant and how we interpret their daily routines; through to the Black Death and its dramatic consequences in reshaping society; to Shakespeare’s England, examining the rise of leisure pursuits such as the theatre; finishing with a ‘tale of two cities’ with a comparative study of industrial Britain and revolutionary France.  The second theme for our year seven pupils is ‘power’.  This follows the changes in kingship from Medieval to Tudor England and then the subsequent progress and change from monarchy to a democratic system that we have today.</p>

            <p>Our Year 8 programme of study looks at religion over time with the importance of the Medieval Church, moving through the Reformation and how this changed the political, social and economic landscape of our history. The second term sees the study of modern history, with a focus on the twentieth century. In this unit, pupils’ research key developments and events that have shaped our society today, including the women’s suffrage movement in Britain; the two world wars, with a particular focus on their causes and consequences; then the Holocaust with an emphasis on the questions it raises about humanity. In addition, how local history has been formed as a result of WWII will provide insight into how international affairs affect all members of society. The last programme of study for pupils will be the theme of ‘extreme world’, which will look at revolutions and how these enable pupils to understand how extremism can flourish and its consequences. </p>

            <p>Key Stage 4 students are currently completing a three year course in GCSE History. We offer pupils a development study which looks at ‘Medicine through Time’. This unit sees the study of ancient civilisations to the present day, applying historical concepts to develop pupils understanding of this important theme. Currently, our Year 11 students are completing their in-depth study on the American West, which looks at the treatment of the native Americans and how the USA was shaped into the country it is today with its developments in the nineteenth century. However, current Year 10 and Year 9 pupils will study the rise of Nazi Germany from 1919 – 1945.</p>

            <p>This varied and thought-provoking curriculum is designed to give pupils both depth and breadth of a range of areas to allow them truly value the lessons from the past and enjoy their learning.</p>

            

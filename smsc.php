            <h3>Purpose</h3>
            <p>To prepare students positively for life in modern Britain and promotes the fundamental British values of democracy, the rule of law, individual liberty and mutual respect for and tolerance of those with different faiths and beliefs and for those without faith. This means fully developing the whole child by helping students to grow and develop as people thereby preparing them for the adult world and allowing students to make sense of the world.</p>

            <h3>Aims</h3>
            <ul class="circle">
            <li>Our curriculum offers daily opportunities for our pupils’ spiritual, moral, social and cultural development. Across all of the different subject areas, pupils are given time to explore their beliefs and reflect upon their reasoning.  At the heart of subject are core values centred on developing our students’ beliefs, allowing them to build their own character and helping them to become young adults who have well-considered values and ideas. </li>

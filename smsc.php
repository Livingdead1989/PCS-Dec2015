            <h3>Areas of Study</h3>
            <p><strong>Assemblies</strong> – At Park Community school we have regular assemblies where students gather together for a common purpose. The areas covered include a wide variety of which are closely matched in content to different areas of the curriculum.  There are common termly themes throughout the year which are underpinned by specific local, British or worldwide celebration and awareness days. This means that there is a coherence to the delivery of SMSC across the school as a community.</p>
            <p><strong>Butterfly Broadcasts</strong> – This is approximately an 8 minute broadcast shown during the week in an allotted mentoring slot. It covers current issues and trending features as well as events and ideas that are developing across the school.  It is an important feature of the week in that it allows students to access current issues that they may not necessarily have any knowledge of.</p>
            <p><strong>Mentoring</strong> – Mentors have a specific slot during the Tuesday amphitheatre time where they mentor students on a one to one basis.  These sessions will focus on academic achievement and progress as well as community and extra-curricular involvement. It is an essential part of life at Park as we believe that good solid relationships should be forged between at least one adult and student in their time at school.  This means that they always feel safe and supported in the school environment.</p>
            <p><strong>Challenge days</strong> – Challenge days are whole days off curriculum where students learn and take part in activities that are over and above their normal working day. The topics covered include Britishness, tolerance, Sex and relationships education, citizenship, making informed choices with regards alcohol and drugs as well as career choices.  They get to listen to outside speakers with real life experiences in the fields that they are exploring which broadens their horizons in so that they are fully equipped to face the world when they leave school.</p>
            <p><strong>A Rights Respecting School</strong> – In a Rights Respecting School the pupils have an opportunity to learn about the United Nations Convention on the Rights of the Child through family assemblies and pupil led groups. As a rights respecting school we do not only teach our pupils about children’s rights but we also model rights and respect in all our relationships: between staff, between staff and pupils, and between pupils.</p>


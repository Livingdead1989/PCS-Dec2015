        	<h3>Purpose</h3>
			<p>Studying Drama requires emotional maturity, and gives students an understanding of themselves. It involves using not only their voices and bodies, but also emotions and creativity has the ability to develop confidence and self esteem . The subject requires a great deal of peer trust, and so it plays an important role in teaching communication, listening and empathy skills. Studying drama is demanding, and teaches students that success only comes from hard work. The skills that are learnt by studying drama are invaluable in later life, for example, being able to speak and present confidently in front of people, and the planning that is required in any production, all are useful in many careers.</p>
			<h3>Aims</h3>
			<ul class="circle">Drama will encourage the students to develop:
				<li>Skills in creativity, physical competence, self-confidence, concentration, self-discipline, and communication.</li>
				<li>An appreciation of empathy and sensitivity.</li>
				<li>An understanding of the benefits of participation in the arts, performance and creativity whilst at Park Community School and throughout their life.</li>
				<li>An appreciation of skillful and creative performances through improvisation, rehearsal, performance, evaluation, appreciation and assessment.</li>
				<li>An understanding of communication through voice and movement.</li>
				<li>An appreciation of the ways in which playwrights achieve their effects and communicate their intentions to an audience.</li>
				<li>An awareness of social, historical and cultural contexts and influences within plays and other style of dramatic presentation.</li>
				<li>Self and group awareness, personal responsibility, negotiation and teamwork skills.</li>
				<li>Evaluation of their own and other’s work.</li>
			</ul>
			<ul class="circle">Drama will also contribute towards the development of:
				<li>Problem solving skills (by giving pupils the opportunity to devise, refine and create their own work).</li>
				<li>Self-esteem through confidence in performance.</li>
				<li>Inter-personal skills (by helping students to be aware of their roles as members of groups, and through taking into account other pupil’s thoughts and ideas).</li>
				<li>Functional skills which will enable them to operate confidently, effectively and independently in life and work.</li>
				<li>Personal, learning and thinking skills (being independent enquirers, creative thinkers, reflective learners, teamworkers, self managers and effective participants).</li>
			</ul>
			<h3>Areas of Study</h3>
			<h3>Key Stage 3</h3>
			<p>In Year 7 students are introduced to the basic conventions of theatre and storytelling, exploring the ‘mythical quest’ of Coriander a child tasked with embarking on a journey of discovery. In these lessons, students will develop group work and problem-solving abilities alongside dramatic techniques. The emphasis is on mastering the skills required to perform in character, with confidence to a small audience of their peers.</p>
			<p>Once in Year 8, all students explore ways that movement can be used within performance to communicate meaning to an audience. From Simple Mime and body language techniques to more stylized work like Melodrama, Slapstick Comedy and Physical theatre, this scheme aims to develop students self confidence in moving with control in a range of scenes. This topic also covers the basics of technical directing for stage, an important part of study at Key Stage 4.</p>
			<h3>BTEC Technical Award in Performing Arts (Level 1 and 2)</h3>
			<p>With a BTEC Tech Award in Performing Arts, students are able to explore,  challenge and realise their potential. During the course, students can see whether the industry is one they want to be in, where they could go, and gain the knowledge and skills they need to succeed in their next steps.  After completing the course, students can continue on to further  vocational and academic study at level 2 and level 3, as well as  apprenticeships and traineeships. What’s more, the transferable skills students master during their  studies such as self-reflection, communication, teamwork and problem  solving will also support their progress in the present and future.</p>
			<p>Please note: this course requires students to show commitment and dedication both in lessons and beyond. There is an expectation that all Key Stage 4 Performing Arts students will attend additional rehearsals as necessary to complete practical work to the best of their ability.</p>
			<p>The course has two internally assessed components and one that's externally assessed:</p>
			<h4>Component 1 – Exploring the performing arts</h4>
			<ul class="circle">During Component 1,  students will observe and reproduce existing professional plays through live and recorded performances, exploring:
				<li>performance styles, creative intentions and purposes for performances</li>
				<li>performance roles, responsibilities  and skills both on and off stage</li>
				<li>performance techniques, approaches and processes for specific styles and practitioners</li>
				<li>how practitioners create and influence what’s performed.</li>
			</ul>
			<p>Evidence for this unit comes from recordings of practical workshops, ongoing research journals and a final PowerPoint presentation comparing styles explored.</p>
			<h4>Component 2 – Developing performance skills</h4>
			<ul class="circle">During Component 2, your students will:
				<li>gain physical, interpretative,  vocal and rehearsal skills during  workshops and classes</li>
				<li>apply their technical, stylistic and interpretative skills in performances</li>
				<li>reflect on their progress and use of skills in performance, as well as how they could improve</li>
			</ul>
			<p>Evidence for this unit consists of an ongoing logbook for acting skill development, recordings of rehearsals and final performance in a public showcase.</p>
			<h4>Component 3 – Performing to a brief. </h4>
			<p>Students consider how practitioners adapt their skills for different contexts and put this into practice in an externally assessed performance. Students work in groups of between 3 and 7 members to create a performance based on a set brief.</p>
			<ul class="circle">To do this, students will:
				<li>use the brief and what they’ve learned to come up with ideas for the performance</li>
				<li>choose the skills and techniques they’ll need</li>
				<li>build on their skills in classes, workshops and rehearsals</li>
				<li>review the development process within an ideas and skills log</li>
				<li>perform a piece lasting 10–15 minutes (which is filmed) to their chosen target audience</li>
				<li>reflect on the performance in an evaluation report.</li>
			</ul>
			<p>For further information on this course, including examples of assessment materials please visit; <a href="//qualifications.pearson.com/en/qualifications/btec-tech-awards/performing-arts.coursematerials.html#filterQuery=category:Pearson-UK:Category%2FSpecification-and-sample-assessments" target="_blank">Pearson Qualifications</a>.</p>
			<h3>Additional Courses and Experiences available</h3>
			<p><strong>LAMDA lessons and examinations in Acting</strong> - Examinations designed to develop the skills necessary to communicate dramatic text to an audience. Learners who prepare themselves appropriately will develop Interpretative skills, technical skills and knowledge of the performance process. These lessons are additional to the standard drama curriculum and examination fees apply (with concession for those in receipt of Free School Meals).</p>
			<h3>Further Information</h3>
			<p><strong>School Production (Autumn Term)</strong> - Each year, the Performing Arts faculty produces a full scale musical performance open to the public. The aim is to give pupils experience of working in a disciplined and highly focused manner, encouraging them to develop the attributes of a professional performer. Auditions are held for both lead and ensemble performers and are open to students from all years. Recent shows include 'Grease', 'Oliver!' and ‘Beauty and the Beast’.</p>
			<p><strong>Additional projects:</strong> In recent years, students have also participated in National and local projects such as Shakespeare Schools Festival,  National Connections and Play for a Day. We also provide students with opportunities to perform their class work to a public audiences in order to complete assessments.</p>
			<h3>Key Stage 3</h3>
			<ul class="circle">Main topics students study:
				<li>The Godstone – Storytelling, improvisation and problem solving.</li>
				<li>Action Packed – Body Language, Mime, Slapstick, Melodrama, staging, devising from a stimulus.</li>
			</ul>
			<ul class="circle">Main skills students develop:
				<li>Improvising and devising</li>
				<li>Characterisation</li>
				<li>Teamwork</li>
				<li>Basic drama skills and techniques</li>
				<li>Movement control</li>
				<li>Problem solving</li>
				<li>Audience Awareness</li>
				<li>Communication</li>
			</ul>
			<h3>Keystage 4 - BTEC Vocational</h3>
			<ul class="circle">Main topics students study:
				<li>Exploring the performing Arts</li>
				<li>Developing technical skills</li>
				<li>Performing to a brief.</li>
			</ul>
			<ul class="circle">Main skills students develop:
				<li>Characterisation</li>
				<li>Theatrical styles and genres</li>
				<li>Technical Movement and voice skills</li>
				<li>Directing Skills</li>
				<li>Industry awareness</li>
				<li>Teamwork</li>
				<li>Communication</li>
				<li>Analysis and evaluation</li>
			</ul>

            <h3>Purpose</h3>
            <p>Studying Drama requires emotional maturity, and gives students a deep understanding of themselves. Because it involves using not only their voices and bodies, but also emotions and creativity, it is able to engender a deep sense of self-esteem. It is also a subject that requires a great deal of peer trust, and so it plays an important role in teaching communication, listening and empathy skills. Studying drama is demanding, and teaches students that success only comes from hard work.   The skills that are learnt by studying drama are invaluable in later life, for example, being able to speak and present confidently in front of people, and the planning that is required in any production, all are useful in many careers.</p>

            <h3>Aims</h3>
            <p>Drama will encourage the students to develop:</p>
            <ul class="circle">
                <li>Skills in creativity, physical competence, self-confidence, concentration, self-discipline, and communication.</li>
                <li>An appreciation of empathy and sensitivity.</li>
                <li>An understanding of the benefits of participation in the arts, performance and creativity whilst at Park Community School and throughout their life.</li>
                <li>An appreciation of skilful and creative performances through improvisation, rehearsal, performance, evaluation, appreciation and assessment.</li>
                <li>An understanding of communication through voice and movement.</li>
                <li>An appreciation of the ways in which playwrights achieve their effects and communicate their intentions to an audience.</li>
                <li>An awareness of social, historical and cultural contexts and influences within plays and other style of dramatic presentation.</li>
                <li>Self and group awareness, personal responsibility, negotiation and teamwork skills.</li>
                <li>Evaluation of their own and other’s work.</li>
            </ul>
            <p>Drama will also contribute towards the development of:</p>
            <ul class="circle">
                <li>Problem solving skills (by giving pupils the opportunity to devise, refine and create their own work).</li>
